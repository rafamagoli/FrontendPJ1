<template>
    <div id="task-update">
      <div class="main-content">
        <section class="add-task-form">
          <h2>Update Task</h2>
          <form @submit.prevent="handleSubmit">
            <!-- Employee Selector -->
            <div class="form-group">
              <label for="employee">Assigned to</label>
              <select id="employee" v-model="task.employee" required>
                <option value="" disabled>Select an employee</option>
                <option
                  v-for="employee in employees"
                  :key="employee"
                  :value="employee"
                >
                  {{ employee }}
                </option>
              </select>
            </div>
  
            <!-- Title -->
            <div class="form-group">
              <label for="title">Title</label>
              <input id="title" v-model="task.title" type="text" required />
            </div>
  
            <!-- Description -->
            <div class="form-group">
              <label for="description">Description</label>
              <input
                id="description"
                v-model="task.description"
                type="text"
                required
              />
            </div>
  
            <!-- Limit Date -->
            <div class="form-group">
              <label for="limitDate">Limit Date</label>
              <input
                id="limitDate"
                v-model="task.limitDate"
                type="date"
                required
              />
            </div>
  
            <!-- Mark as Complete Checkbox -->
            <div class="form-group checkbox-group">
              <label>
                <input
                  type="checkbox"
                  v-model="task.completed"
                  class="complete-checkbox"
                />
                Mark as Complete
              </label>
            </div>
  
            <!-- Action Buttons -->
            <div class="form-actions">
              <button type="button" class="cancel-button" @click="cancel">
                Cancel
              </button>
              <button type="submit" class="create-button">Update</button>
            </div>
          </form>
        </section>
      </div>
    </div>
  </template>
  
  <script>
  export default {
    data() {
      return {
        task: {
          id: null,
          title: "",
          employee: "",
          description: "",
          limitDate: "",
          completed: false,
        },
        employees: [
          "Bulma Garcia",
          "Pepper Stark",
          "Martini Silva",
          "Sansa Stark",
          "Roberto Silva",
          "Rafaela Oliveira",
          "Ana Garcia",
          "Caio Lacerda",
          "Lucas Oliveira",
          "Tony Stark",
          "Pepper Potts",
        ],
      };
    },
    created() {
      // Get the task data from route state
      const taskData = this.$route.state?.task;
      if (taskData) {
        this.task = { ...taskData };
      } else {
        console.error("No task data received. Redirecting...");
        this.$router.push("/task/list");
      }
    },
    methods: {
      handleSubmit() {
        if (
          !this.task.employee ||
          !this.task.title ||
          !this.task.description ||
          !this.task.limitDate
        ) {
          alert("All fields are required!");
          return;
        }
        console.log("Task updated:", this.task);
        this.$router.push("/task/list");
      },
      cancel() {
        this.$router.push("/task/list");
      },
    },
  };
  </script>
  
